while True:
    num = int(input("Ingrese un número entre 1 y 7: "))

    if num == 1:
        print("Lunes")
    elif num == 2:
        print("Martes")
    elif num == 3:
        print("Miércoles")
    elif num == 4:
        print("Jueves")
    elif num == 5:
        print("Viernes")
    elif num == 6:
        print("Sábado")
    elif num == 7:
        print("Domingo")
    else:
        print("El número ingresado no está dentro del rango solicitado.")
